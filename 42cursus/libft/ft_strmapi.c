#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		s_length;
	int		i;
	char	*result;

	if (s == NULL || f == NULL)
		return (NULL);
	s_length = ft_strlen(s);
	result = (char *)malloc(sizeof(char) * (s_length + 1));
	if (result == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
